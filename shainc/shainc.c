/* Helper Macros */
#define ROTR(x, n) ((x >> n) | (x << (32 - n)))
#define CH(x, y, z) ((x & y) ^ (~x & z))
#define MAJ(x, y, z) ((x & y) ^ (x & z) ^ (y & z))
#define SIGMA0(x) (ROTR(x, 2) ^ ROTR(x, 13) ^ ROTR(x, 22))
#define SIGMA1(x) (ROTR(x, 6) ^ ROTR(x, 11) ^ ROTR(x, 25))
#define sigma0(x) (ROTR(x, 7) ^ ROTR(x, 18) ^ (x >> 3))
#define sigma1(x) (ROTR(x, 17) ^ ROTR(x, 19) ^ (x >> 10))

/* SHA-256 Processing Functions */
static void process_chunk(unsigned long *hash, const unsigned char *chunk) {
    unsigned long W[64];
    unsigned long a, b, c, d, e, f, g, h;
    int i;

    /* Prepare message schedule */
    for (i = 0; i < 16; i++) {
        W[i] = (chunk[i * 4] << 24) | (chunk[i * 4 + 1] << 16) | (chunk[i * 4 + 2] << 8) | chunk[i * 4 + 3];
    }
    for (; i < 64; i++) {
        W[i] = sigma1(W[i - 2]) + W[i - 7] + sigma0(W[i - 15]) + W[i - 16];
    }

    /* Initialize working variables */
    a = hash[0]; b = hash[1]; c = hash[2]; d = hash[3];
    e = hash[4]; f = hash[5]; g = hash[6]; h = hash[7];

    /* Compression function */
    for (i = 0; i < 64; i++) {
        unsigned long temp1 = h + SIGMA1(e) + CH(e, f, g) + K[i] + W[i];
        unsigned long temp2 = SIGMA0(a) + MAJ(a, b, c);
        h = g; g = f; f = e;
        e = d + temp1;
        d = c; c = b; b = a;
        a = temp1 + temp2;
    }

    /* Update hash values */
    hash[0] += a; hash[1] += b; hash[2] += c; hash[3] += d;
    hash[4] += e; hash[5] += f; hash[6] += g; hash[7] += h;
}

int main(int argc, char *argv[]) {
    if (argc != 2) {
        printf("Usage: %s <filename>\n", argv[0]);
        return 1;
    }

    FILE *file = fopen(argv[1], "rb");
    if (!file) {
        printf("Error: Cannot open file %s\n", argv[1]);
        return 1;
    }
    
    unsigned char buffer[64];
    size_t bytes_read;
    while ((bytes_read = fread(buffer, 1, 64, file)) > 0) {
        process_chunk(H, buffer);
    }
    fclose(file);
    
    /* Print final hash */
    for (int i = 0; i < 8; i++) {
        printf("%08lx", H[i]);
    }
    printf("\n");

    return 0;
}
